// Helper function to get blog by slug
export const getBlogBySlug = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

// Helper function to get blogs by category
export const getBlogsByCategory = (category) => {
  if (category === "*") return blogPosts;
  return blogPosts.filter(post => post.tag.toLowerCase() === category.toLowerCase());
};

// Helper function to get recent blogs (excluding current one)
export const getRecentBlogs = (currentSlug, limit = 3) => {
  return blogPosts
    .filter(post => post.slug !== currentSlug)
    .slice(0, limit);
};



